    private void btn_1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_1ActionPerformed
        String mensaje = txt_1.getText();
        BigInteger E = new BigInteger(this.txt_2.getText());
        BigInteger N = new BigInteger(this.txt_3.getText());
        RSA enc = new RSA();
        enc.setE(E);
        enc.setN(N);
        BigInteger[] textoCifrado = enc.encriptar(mensaje);
        
        
        for (int i = 0; i < textoCifrado.length; i++) {
            cadena = cadena + textoCifrado[i];
            if (i != textoCifrado.length - 1) {
                cadena = cadena + " ";
            }
        }
        
        pi = textoCifrado.length;
        System.out.println(pi);
        this.txtA_1.setText(cadena);
        
        
    }//GEN-LAST:event_btn_1ActionPerformed

